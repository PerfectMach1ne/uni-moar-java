package Cwiczenie4_5;

public class SzufladaC {
    private int skarpetyCount;
    private int bieliznaCount;
    private int sukienkaCount;
    private int spodnieCount;
    private int garniturCount;
    private int koszulkaCount;

    public SzufladaC(int skarpetyCount, int bieliznaCount, int sukienkaCount, int spodnieCount, int garniturCount, int koszulkaCount) {
        this.skarpetyCount = skarpetyCount;
        this.bieliznaCount = bieliznaCount;
        this.sukienkaCount = sukienkaCount;
        this.spodnieCount = spodnieCount;
        this.garniturCount = garniturCount;
        this.koszulkaCount = koszulkaCount;
    }

    public int getSkarpetyCount() {
        return skarpetyCount;
    }

    public void setSkarpetyCount(int skarpetyCount) {
        this.skarpetyCount = skarpetyCount;
    }

    public int getBieliznaCount() {
        return bieliznaCount;
    }

    public void setBieliznaCount(int bieliznaCount) {
        this.bieliznaCount = bieliznaCount;
    }

    public int getSukienkaCount() {
        return sukienkaCount;
    }

    public void setSukienkaCount(int sukienkaCount) {
        this.sukienkaCount = sukienkaCount;
    }

    public int getSpodnieCount() {
        return spodnieCount;
    }

    public void setSpodnieCount(int spodnieCount) {
        this.spodnieCount = spodnieCount;
    }

    public int getGarniturCount() {
        return garniturCount;
    }

    public void setGarniturCount(int garniturCount) {
        this.garniturCount = garniturCount;
    }

    public int getKoszulkaCount() {
        return koszulkaCount;
    }

    public void setKoszulkaCount(int koszulkaCount) {
        this.koszulkaCount = koszulkaCount;
    }
}
